STANDALONE_UNITS: tuple[tuple[str, str], ...] = (
    (r"\bкм/ч\b", "километров в час"),
    (r"\bм/с\b", "метров в секунду"),
    (r"\bс\.?\s*ут\.?\b", "суток"),
)


def expand_abbreviations(text: str) -> str:
    result = text
    for pattern, replacement in PDD_ABBREVIATIONS:
        result = re.sub(pattern, replacement, result, flags=re.IGNORECASE)
    return result


def expand_standalone_units(text: str) -> str:
    result = text
    for pattern, replacement in STANDALONE_UNITS:
        result = re.sub(pattern, replacement, result, flags=re.IGNORECASE)
    return result
